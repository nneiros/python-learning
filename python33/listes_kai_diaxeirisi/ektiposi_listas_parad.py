print ('Εκτυπωση οχτω αριθμων=', list(range (8)))# εκτυπωση ενος συνολου αριθμων
print ('Εκτυπωση απο πεντε μεχρι οχτω=', list(range (5,15)))#εκτυπωση ενος συνολου αριθμων
print ('Εκτυπωση απο 0 εως 30 με βημα 5=', list(range (0,30,5))) #τo 5 ειναι βημα,αν βαλω κομα στο τελος εκτυπωνει και την αλλη γραμμη μαζι
