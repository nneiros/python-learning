import turtle
myTurtle = turtle.Turtle()
myTurtle.circle(50)

myTurtle.penup()
myTurtle.setposition(-60, 0)
myTurtle.setheading(0)
myTurtle.pendown()
myTurtle.color("blue")
myTurtle.write("Nικος ολυμπιακη επιτροπη", font=("Arial", 16, "bold")) 

turtle.getscreen()._root.mainloop()



